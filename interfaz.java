    public void probarLexerFile() throws IOException{
        int contIDs=0;
        tokenslist = new LinkedList<identificador>();
        File fichero = new File ("fichero.txt");
        PrintWriter writer;
        try {
            writer = new PrintWriter(fichero);
            writer.print(jTextArea1.getText());
            writer.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(interfaz.class.getName()).log(Level.SEVERE, null, ex);
        }
        Reader reader = new BufferedReader(new FileReader("fichero.txt"));
        Lexer lexer = new Lexer (reader);
        String resultado="";
        while (true){
            Token token =lexer.yylex();
            if (token == null){
                for(int i=0;i<tokenslist.size();i++){
                    System.out.println(tokenslist.get(i).nombre + "=" + tokenslist.get(i).ID);
                }
                jTextArea2.setText(resultado);
                return;
            }
            switch (token){
                case INICIO: 
                    resultado= resultado+"<inicio>";
                    break;
                case FIM:
                    resultado = resultado+"<fim>";
                    break;
                case CLASSE: 
                    resultado = resultado+"<classe>";
                    break;
                case ESCREVA:
                    resultado = resultado+"<escreva>";
                    break;
                case IF:
                    resultado = resultado + "<if>";
                    break;
                case FIMIF:
                    resultado = resultado + "<fimif>";
                    break;
                case FOR:
                    resultado = resultado + "<for>";
                    break;
                case FIMFOR:
                    resultado = resultado + "<fimfor>";
                    break;
                case SOMA:
                    resultado=resultado+ "<+>";
                    break;
                case MENOS:
                    resultado=resultado+ "<->";
                    break;
                case MULT:
                    resultado=resultado+ "<*>";
                    break;
                case DIV:
                    resultado=resultado+ "</>";
                    break;
                case MAIOR:
                    resultado= resultado+"< > >";
                    break;
                case MENOR:
                    resultado= resultado+"< < >";
                    break;
                case ATRIBUI:
                    resultado= resultado+ "<==>";
                    break;
                case ABREP:
                    resultado= resultado+ "<(>";
                    break;
                case FECHAP:
                    resultado = resultado+ "<)>";
                    break;
                case IGUAL:
                    resultado=resultado+ "<=>";
                    break;
                case ERROR:
                    resultado=resultado+ "Error, simbolo nao reconhecido ";
                    break;
                case ID: {
                    contIDs++;
                    identificador tokenitem=new identificador();
                    tokenitem.nombre=lexer.lexeme;
                    tokenitem.ID=contIDs;
                    tokenslist.add(tokenitem);
                    resultado=resultado+ "<ID" + contIDs + "> ";
                    break;
                }
                case INT:
                    resultado=resultado+ "< " + lexer.lexeme + "> ";
                    break;
                default:
                    resultado=resultado+ "<"+ lexer.lexeme + "> ";
            }
    }
 }
    public void tablaResultado(){
        Object[][] matriz = new Object [tokenslist.size()][2];
        for(int i =0; i<tokenslist.size();i++){
            matriz[i][0] = tokenslist.get(i).nombre;
            matriz[i][1] = tokenslist.get(i).ID;
        }
        jTable1.setModel(new javax.swing.table.DefaultTableModel(matriz,
            new String [] {
                "Nombre", "ID"
        }
        ));
    }
